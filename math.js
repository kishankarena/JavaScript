// sign() method
let num1 = 5.55 ;
console.log(Math.sign(num1)); // 1
let num2 = -5.20;
console.log(Math.sign(num2));  //-1
let num3 =0;
console.log(Math.sign(num3));  // 0
let num4 = -0;
console.log(Math.sign(num4)); //-0

